          <h2>How is blood pressure measured?</h2>
          <hr />
<p>Blood pressure is measured in millimetres of mercury (mmHg) and is given as 2 figures:</p>
             <hr />
<ul>
  <li>systolic pressure – the pressure when your heart pushes blood out</li>
  <li>diastolic pressure – the pressure when your heart rests between beats</li>
</ul>
<p>For example, if your blood pressure is "140 over 90" or 140/90mmHg, it means you have a systolic pressure of 140mmHg and a diastolic pressure of 90mmHg.
</p>
<p>As a general guide:</p>
<ul>
  <li>ideal blood pressure is considered to be between 90/60mmHg and 120/80mmHg</li>
  <li>high blood pressure is considered to be 140/90mmHg or higher</li>
  <li>low blood pressure is considered to be below 90/60mmHg</li>
</ul>
